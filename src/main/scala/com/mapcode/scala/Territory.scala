/**
 * This class defines the available territory codes as used by mapcode.
 */
case class Territory(delegate: JTerritory) {

  def fullName: String =
    delegate.getFullName

  def parentTerritory: Option[Territory] =
    delegate.getParentTerritory match {
      case null => None
      case parent => Some(parent)
    }

  def aliases: Seq[String] =
    delegate.getAliases

  def fullNameAliases: Seq[String] =
    delegate.getFullNameAliases

  def toAlphaCode(format: Territory.AlphaCodeFormat, alphabet: Alphabet): String =
    delegate.toAlphaCode(format, alphabet)

  def toAlphaCode(format: Territory.AlphaCodeFormat): String =
    delegate.toAlphaCode(format)

  def isSubdivision: Boolean =
    delegate.isSubdivision

  def hasSubdivisions: Boolean =
    delegate.hasSubdivisions

  override def toString =
    delegate.toString

  def toString(alphabet: Alphabet) =
    delegate.toString(alphabet)
}

